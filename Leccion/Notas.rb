class Notas

	def initialize(nombre, parcial, final, mejoramiento, promedio)
		@nombre = nomre
		@parcial = parcial
		@final = final
		@mejoramiento = mejoramiento
		@promedio = promedio
	end

	def getNombre
		@nombre
	end

	def setNombre(nuevoNombre)
		@nombre = nuevoNombre
	end

	def getParcial
		@parcial
	end

	def setParcial(nuevoParcial)
		@parcial = nuevoParcial
	end

	def getFinal
		@final
	end

	def setFinal(nuevoFinal)
		@final = nuevoFinal
	end

	def getMejoramiento
		@mejoramiento
	end

	def setMejoramiento(nuevoMejoramiento)
		@mejoramiento = mejoramiento
	end

	def getPromedio
		@promedio
	end

	def setPromedio(nuevoPromedio)
		@promedio = nuevoPromedio
	end
end